Cypress.Commands.add('Login', user =>{
    cy.get('.login-form')
    cy.get('[data-cy="email-input"]')
        .should('be.visible')
        .type(user.email)
    cy.get('[data-cy="password-input"]')
        .should('be.visible')
        .type(user.password)
    cy.get('[data-cy="submit-button"]')
        .should('be.visible')
        .click()
    cy.get('[data-cy="welcome-message"]')
        .should('be.visible')
        .contains('Welcome back!')
})